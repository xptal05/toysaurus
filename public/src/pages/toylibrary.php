            <h2>1. Vyberte si hračky, se kterými si chcete hrát</h2>
            Po úspěšné registraci (info zde) do spolku toysaurus održíte 10 Toysaurus bodů, které budete moci proměňovat ve hračky.
            V online katalogu nebo také v knihovně (od konce roku 2022) si poté podle svých představ začněte vybírat z naší hračkové sbírky. Většinu hraček máme ve více exemplářích, aby se pokud možno dostalo na každého. Hračky pro vás průběžně doplňujeme, takže vždycky narazíte na něco nového.
        </div>
        <div>
            <h2>2. Půjčte si hračky domů</h2>
            Po složení vratné zálohy dostanete potvrzení o tom, jaké hračky máte zapůjčené a datum do kdy musí být vráceny. Doma si zkontrolujete, že jsou hračky kompletní, a pak si jen užívejte klidu, kdy vaše děti objevují “nové” hračkami.
            Hračky si nechte tak dlouho, jak si budete přát. Můžete je mít doma po celou výpůjční dobu nebo dobu kratší. Až budete mít pocit, že jste si jich užili do sytosti, vrátíte nám je zpátky. Buď všechny nebo jen ty, které už u vás doma zahálí.
        </div>
        <div>
            <h2>3. Vraťte půjčené hračky a vyzkoušejte nové</h2>
            Až Vás hračky přestanou bavit, nebo před vypršením výpůjční jejich lhůty, nám je vraťte do knihovny zpět a vyměňte je za jiné.
            Očekáváme, že každý člen vrátí své hračky ve stavu, v jakém by si je chtěl půjčit. Při vrácení všechny hračky řádně zkontrolujeme a dále očistíme prostředky speciálně určenými na dětské hračky.
            Pokud by se vám některá hračka ztratila nebo hodně poškodila, společně najdeme řešení, jak to napravit. Hlavu vám trhat nebudeme.
