<h1>Sponzorství</h1>
<p class="multi-column">Abychom dále mohly nabízet hračky v knihovně všem, stále hledáme sponzory. Podporuje nás několik skvělých organizací, ale potřebujeme každý rok získat finanční prostředky na udržení zásoby a obnovu hraček, pokrytí provozních nákladů a údržbu našich služeb.
</p>
<h2>Proč právě my?</h2>
<ul>
    <li>Jsme první knihovnou hraček v Praze, která nabízí své služby českým i zahraničním rodinám.
    </li>
    <li>Naší zásadou je poskytovat naše služby co největšímu počtu dětí, proto udržujeme členské poplatky na co nejnižší úrovni – náš měsíční poplatek 250,-Kč (cena 2 Happy meal menu v McDonald’s) umoňuje zapůjčení až 5 hraček najednou, členové si je mohou vyměnit za jiné jak často chtějí.
    </li>
    <li>Knihovna hraček Toysaurus je místem, kde mohou členové (zejména maminky na mateřské dovolené) rozvíjet své schopnosti a dovednosti, ale i své projekty a tak se seberealizovat ale i se cítit o něco více zapojenié do společnosti.
    </li>
    <li>Víme co se děje a přizpůsobujeme naše služby aktuální situaci a trendům. Hledáme každou příležitost, jak být pro naši komunitu lepší.
    </li>
    <li>Knihovna hraček Toysaurus nabízí možnost jak bez větší námahy snížit dopad na naši planetu, takže generace, kterou vychováváme, si ji bude moci užít.
    </li>
    <li>Váš dar nám nesmírně pomáhá a pokud je vyšší než 2000 Kč (pro firmy) či 1000 kč (pro fyzické osoby) tak je daňově uznatelný dle zákona o daních z příjmu § 15, odst.
    </li>
</ul>
